#include <iostream>

using namespace std;

int main()
{
    int t;
    cin>>t;
    while(t--){
        int n,m=0;
        cin>>n;
        int a[n][6];
        for(int i=0;i<n;i++)
            cin>>a[i][0]>>a[i][1]>>a[i][2]>>a[i][3]>>a[i][4]>>a[i][5];
        int b[n][12];
        for(int i=0;i<n;i++){
            b[i][0]=(a[i][0]*100000+a[i][1]*10000+a[i][2]*1000+a[i][3]*100+a[i][4]*10+a[i][5]);
            b[i][1]=(a[i][1]*100000+a[i][2]*10000+a[i][3]*1000+a[i][4]*100+a[i][5]*10+a[i][0]);
            b[i][2]=(a[i][2]*100000+a[i][3]*10000+a[i][4]*1000+a[i][5]*100+a[i][0]*10+a[i][1]);
            b[i][3]=(a[i][3]*100000+a[i][4]*10000+a[i][5]*1000+a[i][0]*100+a[i][1]*10+a[i][2]);
            b[i][4]=(a[i][4]*100000+a[i][5]*10000+a[i][0]*1000+a[i][1]*100+a[i][2]*10+a[i][3]);
            b[i][5]=(a[i][5]*100000+a[i][0]*10000+a[i][1]*1000+a[i][2]*100+a[i][3]*10+a[i][4]);
            b[i][6]=(a[i][5]*100000+a[i][4]*10000+a[i][3]*1000+a[i][2]*100+a[i][1]*10+a[i][0]);
            b[i][7]=(a[i][4]*100000+a[i][3]*10000+a[i][2]*1000+a[i][1]*100+a[i][0]*10+a[i][5]);
            b[i][8]=(a[i][3]*100000+a[i][2]*10000+a[i][1]*1000+a[i][0]*100+a[i][5]*10+a[i][4]);
            b[i][9]=(a[i][2]*100000+a[i][1]*10000+a[i][0]*1000+a[i][5]*100+a[i][4]*10+a[i][3]);
            b[i][10]=(a[i][1]*100000+a[i][0]*10000+a[i][5]*1000+a[i][4]*100+a[i][3]*10+a[i][2]);
            b[i][11]=(a[i][0]*100000+a[i][5]*10000+a[i][4]*1000+a[i][3]*100+a[i][2]*10+a[i][1]);
        }
        for(int i=0;i<n;i++)
        for(int j=i+1;j<n;j++){
            for(int k=0;k<12;k++){
                if(b[i][0]==b[j][k]){
                    m=1;
                    break;
                }
                if(m==1)break;
            }
            if(m==1)break;
        }
        if(m)cout<<"Twin snowflakes found."<<endl;
        else cout<<"No two snowflakes are alike."<<endl;
    }
    return 0;
}
